package Program;

public class Looping {

	public static void main(String[] args) {
		System.out.println("Hello world"); // it is not professional way to print repetitive output
		System.out.println("Hello world");
		System.out.println("Hello world");
		System.out.println("Hello world");
		System.out.println("Hello world");

		System.out.println("looping ");

		for (int i = 0; i < 5; i++) {
			System.out.println("Hello world");

		}

	}

}
