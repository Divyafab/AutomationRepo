package Program;

public class MultiplicationTables {
	public static void main(String[] args) {
		int num=9;
		
		for(int i=1; i<=10; i++) {
			 int c = num*i;
			 System.out.println(num+ "*"+i+ "=" +c);
		}
	}
	
	
}
